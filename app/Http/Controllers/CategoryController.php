     * @return \Illuminate\Http\Response
     */
    public function index()
    {

        $category = DB::table('categories as c1')
            ->leftJoin('categories as c2', 'c1.parent_cat_id', '=', 'c2.id')
            ->select('c1.*', 'c2.name as parent_category_name')
            ->get();

        return CategoryCollection::collection($category);
    }

